function M.big_text(text)
  text = string.upper(text)
  local lines = { "", "", "", "", "", "" }
  for i = 1, #text do
    local ch = text:sub(i, i)
    local glyph = FONT[ch] or FONT[" "]
    for row = 1, 6 do
      lines[row] = lines[row] .. glyph[row] .. "  "
    end
  end
  return lines
end

return M
